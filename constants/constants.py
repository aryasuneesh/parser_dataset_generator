def count_paths_with_depth():
    total = 0
    paths_with_depth = []
    
    # Count all paths from each array
    for var_name, var_list in [
        ('exposure', exposure),
        ('attribute', attribute),
        ('vehicle', vehicle),
        ('asset_type', asset_type),
        ('sebi_classification', sebi_classification),
        ('objective', objective)
    ]:
        valid_paths = [(var_name, p) for p in var_list if not p.startswith('#')]  # Only skip commented paths
        total += len(valid_paths)
        paths_with_depth.extend(valid_paths)
        print(f"{var_name}: {len(valid_paths)} valid paths")
    
    return total, paths_with_depth

